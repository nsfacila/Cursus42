#include "libftprintf.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		n = "-";
		n = -n;
	}
	if (n > 9)
	{
		n = n / 10;
		n = n % 10 + '0';
	}
	else
		n = n + '0';
}
